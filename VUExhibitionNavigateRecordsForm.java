    public void First() throws ClassNotFoundException, SQLException{
        CreateStatement();
        
        try{
            rs.first();
            txtStudentNameNav.setText(rs.getString("StudentName"));
            txtRegistrationIDNav.setText(rs.getString("RegistrationID"));
            txtFacultyNav.setText(rs.getString("Faculty"));
            txtProjectTitleNav.setText(rs.getString("ProjectTitleNav"));
            txtContactNumberNav.setText(rs.getString("ContactNumber"));
            txtEmailAddressNav.setText(rs.getString("EmailAddress"));
            
            System.out.println("EMAIL ADDRESS: " + rs.getString("ID"));
            
        }catch(SQLException ex){
        //handling it
        System.out.println("Something went wrong in first Record...!");
        System.out.println("Message: " + ex.getMessage());
        }
        
        
            
    
    
    }
