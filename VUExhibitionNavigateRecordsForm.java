    public void CreateStatement() throws ClassNotFoundException, SQLException{
           
    
     Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            
     conn = DriverManager.getConnection("jdbc:ucanaccess://D:/VU_Exhibition.accdb");
     System.out.println("Database Connected successfully...!");
     
     //SELECTING DATA FROM OUR VU_Exhibition database table, participants
     selTable = "SELECT * FROM participants";
     
     //Excuting our SQL query
     s=conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
     
     s.executeQuery(selTable);
     rs=s.getResultSet();
     System.out.println("EMAIL ADDRESS: " + rs.getString("StudentName"));
    
    
    
    }
    
    
